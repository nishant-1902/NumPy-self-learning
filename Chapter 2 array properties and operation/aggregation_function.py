import numpy as np

arr =  np.array([10,20,30,40,50])

print(np.min(arr)) # returns minimum element in array

print(np.max(arr)) # returns the maximum element in array

print(np.sum(arr)) # return the sum of all elements in array

print(np.mean(arr)) # returns the avarage 

print(np.std(arr)) # returns standard value

print(np.var(arr)) # returns variance